// Helper function to get appropriate image for product
export function getProductImage(category: string, subcategory: string, index: number = 0): string {
  const categoryImages = PRODUCT_IMAGE_MAPPING[category as keyof typeof PRODUCT_IMAGE_MAPPING];
  
  if (!categoryImages) {
    // Fallback to generic image
    return 'https://images.unsplash.com/photo-1560472354-b33ff0c44a43?w=300&h=300&fit=crop';
  }
  
  const subcategoryImages = categoryImages[subcategory as keyof typeof categoryImages] as string[];
  
  if (!subcategoryImages || !Array.isArray(subcategoryImages) || subcategoryImages.length === 0) {
    // Fallback to first available subcategory
    const firstSubcategory = Object.values(categoryImages)[0] as string[];
    if (Array.isArray(firstSubcategory)) {
      return firstSubcategory[index % firstSubcategory.length] || 'https://images.unsplash.com/photo-1560472354-b33ff0c44a43?w=300&h=300&fit=crop';
    }
    return 'https://images.unsplash.com/photo-1560472354-b33ff0c44a43?w=300&h=300&fit=crop';
  }
  
  return subcategoryImages[index % subcategoryImages.length];
}

// Helper function to validate product image matches
export function validateProductImage(product: { category: string; subcategory: string; image: string }): boolean {
  const expectedImage = getProductImage(product.category, product.subcategory, 0);
  const expectedCategory = expectedImage.split('?')[0].split('/').pop()?.split('-')[0];
  const actualCategory = product.image.split('?')[0].split('/').pop()?.split('-')[0];
  
  return expectedCategory === actualCategory;
}
